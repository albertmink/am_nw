public class Ausdruecke1 {
  public static void main (String[] args) {
    System.out.println( "(a): 12.3 + 1 = " + (12.3 + 1) );
    System.out.println( "(b): 0.5*4 = "+(0.5*4)+" (double)" );
    System.out.println( "(g): (int) 1.23 = " + ((int) 1.23) );
    System.out.println( "---------division-------" );
    System.out.println( "(c): 2.0 / 4.0 = " + (2.0/4.0) );
    System.out.println( "(f): 3 / 6.0 = " + (3 / 6.0) );
    System.out.println( "(i): 1.0 / 4 = " + (1.0 / 4) );
    System.out.println( "(d): (2-1.0) / 5 = "+((2-1.0) / 5) );
    System.out.println( "(m): 1 / 2 = " +(1 / 2) + " (int)" );
    System.out.println( "(h): 2/(int)3.14 = " + (2/(int)3.14) );
  }
}
