public class Ausdruecke2 {
  public static void main (String[] args) {
    System.out.println( "---------boolean----------" );
    System.out.println( "(k): 1 > 2 = "+(1>2) );
    System.out.println( "(n): 1 == 2 = "+(1==2) );
    System.out.println( "(o): (1+1)>1 = "+((1+1)>1 ) );
    System.out.println( "---------modulo-----------" );
    System.out.println( "(j): 15 %% 2 = "+(15 % 2) );
    System.out.println( "(e): 14.0%%5 = "+(14.0%5)+" (double)");
    System.out.println( "-----------ascii----------" );
    System.out.println( "(l): 'a'+1 = "+('a'+1 ) + " // 98" );
    System.out.println( "(x): (char)98 = "+((char)98)+" // b" );
    System.out.println( "(y): 'a'+'b' = "+('a'+'b')+" // 195" );
  }
}
